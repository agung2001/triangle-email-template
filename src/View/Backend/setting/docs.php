<div class="row">
    <h1>Triangle - Email Template</h1>
    <p>
        Triangle is an email template editor and builder that can be used to send a beautiful professional design email template to your customers. You can set up your custom email template design by pasting your HTML and CSS into the editor. The plugin will then automatically combine your HTML and CSS script into a standardized email template that will be sent to your customers.
    </p>
    <p>
        You can use the plugin for cases like, sending custom invoice, getting in touch with your customers, sending a notification about payments, etc. You can also modify the pre-built templates within the template lists to match your needs.
    </p>
</div>

<div class="row">
    <h3><i class="fas fa-box"></i> Builder</h3>
    <p>
        Email clients are unpredictable. Even today many clients strip styles that are not inlined, so it is important to inline your CSS before sending. Triangle using custom libraries to inline your CSS script with your HTML.  Don't risk your transactional emails or marketing campaigns falling apart.
    </p>

    <ul>
        <li><a href="https://github.com/tijsverkoyen/CssToInlineStyles">[tijsverkoyen/CssToInlineStyles]</a> - PHP CssToInlineStyles is a class to convert HTML into HTML with inline styles.</li>
    </ul>
</div>

