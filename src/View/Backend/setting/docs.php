<div class="row">
    <h1>Triangle - Email Template</h1>

    <p>
        Triangle is an email template editor and builder that can be used to send a beautiful professional design email template to your customers. You can set up your custom email template design by pasting your HTML and CSS into the editor. The plugin will then automatically combine your HTML and CSS script into a standardized email template that will be sent to your customers.
    </p>
    <p>
        You can use the plugin for cases like, sending custom invoices, getting in touch with your customers, sending a notification about payments, etc. You can also modify the pre-built templates within the template lists to match your needs.
    </p>
</div>

<div class="row">
    <h3><i class="fas fa-box"></i> Vendor and Library</h3>
    <p>
        Triangle using custom libraries to inline your CSS script into your HTML so then the email assets like CSS will be rendered in a customers email client. This will help to avoid assets rendering from the website which will be slowing down the website.
    </p>
    <ul>
        <li><a href="https://github.com/tijsverkoyen/CssToInlineStyles">[tijsverkoyen/CssToInlineStyles]</a> - PHP CssToInlineStyles is a class to convert HTML into HTML with inline styles.</li>
    </ul>
</div>

<div class="row">
    <h3><i class="fas fa-dot-circle"></i> 3rd Party Plugins</h3>
    <p>
        Triangle also supports SMTP configuration which will make your setting up email route easier. You can also using 3rd party plugin like <code>WP MAIL SMTP</code> for your email route.
    </p>
</div>

